package com.example.myapplication;

public class ListViewContentPractice2 {
    int image;
    String name;
    String calories;

    public ListViewContentPractice2(int image, String name, String calories) {
        this.image = image;
        this.name = name;
        this.calories = calories;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getCalories() {
        return calories;
    }
}
